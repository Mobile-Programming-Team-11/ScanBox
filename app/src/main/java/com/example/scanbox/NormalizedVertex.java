package com.example.scanbox;

public class NormalizedVertex { //바운딩 폴리곤의 꼭지점 좌표.
    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}